SELECT
  s.name as subject,
  COUNT(DISTINCT c.id) as chapters,
  COUNT(DISTINCT t.id) as tasks
FROM subjects s
LEFT JOIN chapters c ON s.id = c.subject_id
LEFT JOIN tasks t ON c.id = t.chapter_id
GROUP BY s.id, s.name
HAVING COUNT(DISTINCT t.id) > 0
ORDER BY tasks DESC;
